package com.hk.mm.vo;

public class UserGameResponse {

	private long user_id;
	private String user_image_path;
	private String firstname;
	private String lastname;
	private String game_id;
	private String game_name;
	private String game_image_path;
	public long getUser_id() {
		return user_id;
	}
	public void setUser_id(long user_id) {
		this.user_id = user_id;
	}
	public String getUser_image_path() {
		return user_image_path;
	}
	public void setUser_image_path(String user_image_path) {
		this.user_image_path = user_image_path;
	}
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getGame_id() {
		return game_id;
	}
	public void setGame_id(String game_id) {
		this.game_id = game_id;
	}
	public String getGame_name() {
		return game_name;
	}
	public void setGame_name(String game_name) {
		this.game_name = game_name;
	}
	public String getGame_image_path() {
		return game_image_path;
	}
	public void setGame_image_path(String game_image_path) {
		this.game_image_path = game_image_path;
	}
	
	
}
